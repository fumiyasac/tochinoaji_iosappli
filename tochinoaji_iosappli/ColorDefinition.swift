//このアプリで使用するカラーコードをWebカラーコードで指定
enum ColorDefinition: String {
    
    //基本カラーコード
    case White          = "ffffff"
    case Red            = "cc0000"
    case Green          = "339933"
    case Orange         = "ff9900"
    case Yellow         = "ffd700"
    case Gray           = "cccccc"
    
    //その他カラーコード(お店)
    case GnaviColor     = "ff3300"
    case HotPepperColor = "ff9933"
}
